#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int	last_e;

	last_e = ft_lstsize(lst);
	while (last_e > 1)
	{
		lst = lst->next;
		last_e--;
	}
	return (lst);
}
